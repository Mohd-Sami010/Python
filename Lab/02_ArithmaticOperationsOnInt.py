# 2. WAP to read two integers and perform arithmetic operations on them.

num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))

print(f"{num1} + {num2} = {num1+num2}")
print(f"{num1} - {num2} = {num1-num2}")
print(f"{num1} * {num2} = {num1*num2}")
print(f"{num1} / {num2} = {num1/num2}")